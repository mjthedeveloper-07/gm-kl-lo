const LOTTERY_SERIES = ["Win-Win", "Sthree Sakthi", "Akshaya", "Karunya Plus", "Karunya", "Nirmal", "Pournami"];

const getLotteryName = (date: number): string => {
  return LOTTERY_SERIES[(date - 1) % 7];
};

export const generate2018LotteryData = (): LotteryEntry[] => {
  const results: LotteryEntry[] = [];
  
  Object.entries(CHART_DATA_2018).forEach(([monthStr, dates]) => {
    const month = parseInt(monthStr);
    Object.entries(dates).forEach(([dateStr, result]) => {
      if (result && result !== "******") {
        const date = parseInt(dateStr);
        results.push({
          date: `2018-${month.toString().padStart(2, '0')}-${date.toString().padStart(2, '0')}`,
          result,
          month,
          year: 2018,
          lottery_name: getLotteryName(date),
          draw_number: `W-${2018}-${month}-${date}`,
          lottery_type: "regular"
        });
      }
    });
  });
  
  return results.sort((a, b) => a.date.localeCompare(b.date));
};

export const get2018DataStats = () => {
  const data = generate2018LotteryData();
  return {
    total: data.length,
    dateRange: {
      start: "2018-01-01",
      end: "2018-12-31",
    },
  };
};
